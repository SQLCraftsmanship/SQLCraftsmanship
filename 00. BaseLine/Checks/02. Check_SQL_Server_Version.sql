-- Get SQL Server Version
-----------------------------------------------------------------------------------------------------------------------------------------------

DECLARE @MajorVersion INT; 
  --  9 = 2005     13 = 2016
  -- 10 = 2008/R2  14 = 2017
  -- 11 = 2012     15 = 2019
  -- 12 = 2014

SELECT @MajorVersion = PARSENAME(CONVERT(VARCHAR(32), SERVERPROPERTY('ProductVersion')), 4);

